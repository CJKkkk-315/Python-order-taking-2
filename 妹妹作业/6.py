


































































































































import pandas as pd
import matplotlib.pyplot as plt
plt.rcParams['font.sans-serif'] = ['FangSong']
data = pd.read_excel('名单.xlsx')
BD = data['出生月份']
BD1 = BD.value_counts()
BD2 = dict(BD1)
labels1 = [str(i)+'月' for i in list(BD2.keys())]
explode = [0.01 for i in range(12)]
plt.pie(x=BD2.values(), explode=explode, labels=labels1, autopct='%.2f%%')
plt.show()


